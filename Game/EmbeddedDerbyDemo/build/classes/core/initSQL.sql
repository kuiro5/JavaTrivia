create table courses
(
   course_id smallint not null,
   dept varchar(5),
   number smallint,
   name varchar(30),
   credits smallint,
   num_sections smallint,
   major_notes varchar(30),
   constraint pk_courses primary key (course_id)
);


create table buildings
(
    building_id smallint not null,
    name varchar(40),
    abbrev varchar(6),
    map_loc varchar(2),
    constraint pk_building primary key (building_id)
);

create table sections
(
   sched_no integer not null,
   course_id smallint,
   section smallint,
   building_id smallint,
   room smallint,
   days varchar(7),
   start_time time,
   end_time time,

   constraint pk_sections primary key(sched_no),
   constraint fk_course_id foreign key (course_id) references courses (course_id),
   constraint fk_building_id foreign key (building_id) references buildings (building_id)
);


